#include <iostream>
using namespace std;

int main() {
	//Assign variables
	int x = 10;
	int y = 2;
	
	//Print using cout
	cout << (x + y) << "\n";
	cout << (x - y) << "\n";
	cout << (x / y) << "\n";
	cout << (x % y) << "\n";
	
	//Increment and decrement X
	int s = 20;
	++s;
	cout << s << "\n";
	--s;
	cout << s;
	return 0;
}
